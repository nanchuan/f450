void Lcd_Pic(uint x,uint y,uint w,uint h,uchar *p)
{
  uint j,k;
  Lcd_Set_xy(x,y);
  for(k=0;k<=h;k++)
  {
    for(j=0;j<w;j++)
    {
      Lcd_Write_Byte(p[(k-1)*w+j],1);
    }
    Lcd_Set_xy(x,y+k);
  }
}

void Lcd_String(uint x,uint y,uchar *s)
{
  uint i,c;
  Lcd_Set_xy(x,y);
  while(*s)
  {
    c=(*s)-32;
    for(i=0;i<6;i++)
    {
      Lcd_Write_Byte(font6x8[c][i],1);
    }
    s++;
  }
}

void Lcd_Chinese(uint x,uint y,uchar c)
{
  uint i;
  Lcd_Set_xy(x,y);
  for(i=0;i<12;i++)
  {
    Lcd_Write_Byte(hanzi[c*24+i],1);
  }
  Lcd_Set_xy(x,y+1);  
  for(i=12;i<24;i++)
  {
    Lcd_Write_Byte(hanzi[c*24+i],1);
  }
}

