void Lcd_Write_Byte(uchar dt,uchar com)
{
  uint i;  
  ce_0;
  if(com)
    dc_1;
  else
    dc_0;
  for(i=0;i<8;i++)
  {
    if(dt&0x80)
      din_1;
    else
      din_0;
    dt <<= 1;
    clk_0;
    _NOP();_NOP();
    clk_1;
  }
  dc_1;
  ce_1;
  din_1;
}

void Lcd_Set_xy(uint x,uint y)
{
  Lcd_Write_Byte(0x40|y,0);
  Lcd_Write_Byte(0x80|x,0);
}

void Lcd_Clear()
{
  uint i,j;
  Lcd_Set_xy(0,0);
  for(j=0;j<6;j++)
  {
    for(i=0;i<84;i++)
    {
      Lcd_Write_Byte(0x00,1);
    }
  }
}

void Lcd_bar(uint x,uint y,int n)
{
  uint c = 0xff00;
//  if(n>16) n = 16;
//  if(n<0) n = 0;
  if(n<8){ 
    c >>= n;
    Lcd_Set_xy(x,y);
    Lcd_Write_Byte(c,1);
    Lcd_Set_xy(x,y-1);
    Lcd_Write_Byte(0,1);
  }
  else
  {
    n -= 8;
    c >>= n;
    Lcd_Set_xy(x,y);
    Lcd_Write_Byte(0xff,1);
    Lcd_Set_xy(x,y-1);
    Lcd_Write_Byte(c,1);
  }
}

void Lcd_Char(uint x,uint y,uchar c)
{
  uint i;
  c -= 32;
  Lcd_Set_xy(x,y);
  for(i=0;i<6;i++)
  {
    Lcd_Write_Byte(font6x8[c][i],1);
  }
}

void Lcd_Num(uint x,uint y,int num)
{
  if(num>=0)
    Lcd_Char(x,y,' ');
  else
  {
    Lcd_Char(x,y,'-');
    num=(-num);
  }
  Lcd_Char(x+6,y,num/10000+48);
  Lcd_Char(x+12,y,num%10000/1000+48);
  Lcd_Char(x+18,y,num%1000/100+48);
  Lcd_Char(x+24,y,num%100/10+48);
  Lcd_Char(x+30,y,num%10+48);
}

